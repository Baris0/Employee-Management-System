    private void addButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButton1ActionPerformed
        String nameF = name.getText();
        String surnameF = surname.getText();
        String telNoF = telNumber.getText();
        String eMailF = eMail.getText();
        String tcNumberF = tcNumber.getText();
        String dayF = String.valueOf(day.getSelectedItem());
        String monthF = String.valueOf(month.getSelectedItem());
        String yearF = String.valueOf(year.getSelectedItem());
        String cityF = String.valueOf(city.getSelectedItem());
        String adressF = adress.getText();
        String departmentF = department.getText();
        String salaryF = salary.getText();
        String idF = id.getText();
        String workF = String.valueOf(working.getSelectedItem());
        
        String message = "Lutfen Bilgileri Eksiksiz Giriniz";
        
        if(nameF.isEmpty() && surnameF.isEmpty()){
            JOptionPane.showMessageDialog(this, message);
        }
        else if(telNoF.isEmpty() && eMailF.isEmpty()){
            JOptionPane.showMessageDialog(this, message);
        }
        else if(tcNumberF.isEmpty() && adressF.isEmpty()){
            JOptionPane.showMessageDialog(this, message);
        }
        else if(departmentF.isEmpty() && salaryF.isEmpty()){
            JOptionPane.showMessageDialog(this, message);
        }
        else if(idF.isEmpty()){
            JOptionPane.showMessageDialog(this, message);
        }
        else{
            String datee = date.toString();          
            iDatabaseOperation.addEmplooye(nameF, surnameF, telNoF, eMailF, tcNumberF, dayF, monthF, yearF, cityF, adressF, departmentF, salaryF, idF, workF);
            iDatabaseOperation.loging(datee, idF + " Id Numarali Calisan Eklendi");
            JOptionPane.showMessageDialog(this, "Kayit Eklendi");
            name.setText("");
            surname.setText("");
            telNumber.setText("");
            eMail.setText("");
            tcNumber.setText("");
            adress.setText("");
            department.setText("");
            salary.setText("");
            id.setText("");           
        }
        
    }//GEN-LAST:event_addButton1ActionPerformed

