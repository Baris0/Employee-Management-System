    public void load(){
        model.setRowCount(0);
        ArrayList<Employee> employees = new ArrayList<Employee>();
        employees = iDatabaseOperation.searchEmplooye();
        
        if(employees != null){
            
            for(Employee temp: employees){
                Object[] add = {temp.getName(), temp.getSurname(), temp.getTelNo(), temp.geteMail(), temp.getTcNo(), temp.getBirthday(), temp.getAdress(), temp.getDepartment(), temp.getSalary(), temp.getId(), temp.getWorking()};
                model.addRow(add);
            }                 
        }
        
    }
   
